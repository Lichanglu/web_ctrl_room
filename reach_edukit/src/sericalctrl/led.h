void Change(int fd, char b)
{
	int i;
	char RAM_data[4] = {0};
	for(i=0; i<4; i++)
	{
		switch(b&0xc0)
		{
			case 0x00:
				RAM_data[i]=0x00;
				break;
			case 0x40:
				RAM_data[i]=0x0f;
				break;
			case 0x80:
				RAM_data[i]=0xf0;
				break;
			case 0xc0:
				RAM_data[i]=0xff;
				break;
			default
					:
				break;
		}
		b=b<<2;
		WriteData(fd,RAM_data[i]);
	}
}

