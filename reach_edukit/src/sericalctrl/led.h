int CharToIndex16x16(char c)
{

	// 
	if(c >= 'a' && c <= 'z')
	{
		return (c - 'a' + 10 + 26);
	}
	
	//
	else
		if(c >= '0' && c <= '9')
		{
			return (c - '0');
		}
	//
		else
			if(c >= 'A' && c <= 'Z')
			{
				return (c - 'A' + 10);
			}
			else
				if(c == ':')
				{
					return (c - ':' + 64);
				}
				else
					if(c == '.')
					{
						return (c - '.' + 63);
					}
					else
						if(c == ' ')
						{
							return (c - ' ' + 62);
						}
						else
						{
							return -1;
						}
	return 1;
}

